
def cats(feeling)
  if feeling == "energetic"
    puts "Do 30 second sprint. 2 Pull-ups. 10 Curls and repeat 4-8 times."
    elsif feeling == "tired"
    puts "Do 30 second plank. 30 second bicycle crunch. 30 seconds of Plank Step Ups. 30 second superman. Repeat 4-6 times"
  else 
    puts "Run 2 miles. Then do 4 sets of Pull-ups with 2 reps."
  end
end

cats("happy")